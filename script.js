function algerian() {
    let t = document.getElementById("OwnText");
    let selectedValue = document.getElementById("fstyle").value;

    if (selectedValue === "algerian") {
        t.style.fontFamily = "Algerian"; 
    }else if(selectedValue === "Agency_FB"){
        t.style.fontFamily = "Agency FB";
    }else if(selectedValue === "Kunstler_Script"){
        t.style.fontFamily = "Kunstler Script";
    }else if(selectedValue === "Old_English_Text_MT"){
        t.style.fontFamily = "Old English Text MT";
    }else if(selectedValue === "Times New Roman"){
        t.style.fontFamily = "Times New Roman";
    }else if(selectedValue === "Ink_Free"){
        t.style.fontFamily = "Ink Free";
    }else if(selectedValue === "Wide_Latin"){
        t.style.fontFamily = "Wide Latin";
    }else if(selectedValue === "Goudy_Stout"){
        t.style.fontFamily = "Goudy Stout";
    }else if(selectedValue === "Viner_Hand_ITC"){
        t.style.fontFamily = "Viner Hand ITC";
    }else if(selectedValue === "Vladimir_Script"){
        t.style.fontFamily = "Vladimir Script";
    }else if(selectedValue === "Snap_ITC"){
        t.style.fontFamily = "Snap ITC";
    }else if(selectedValue === "Rage_Italic"){
        t.style.fontFamily = "Rage Italic";
    }else if(selectedValue === "Goudy_Stout"){
        t.style.fontFamily = "Goudy Stout";
    }else if(selectedValue === "Eras_Bold ITC"){
        t.style.fontFamily = "Eras Bold ITC";
    }else if(selectedValue === "Joker_man"){
        t.style.fontFamily = "Jokerman";
    }else if(selectedValue === "Script_MT Bold"){
        t.style.fontFamily = "Script MT Bold";
    }else if(selectedValue === "Matura_MT Script Capitals"){
        t.style.fontFamily = "Matura MT Script Capitals";
    }else if(selectedValue === "Franklin_Gothic Demi Cond"){
        t.style.fontFamily = "Franklin Gothic Demi Cond"
    }else if(selectedValue === "Imprint_MT Shadow"){
        t.style.fontFamily = "Imprint MT Shadow"
    }

    else {
        t.style.fontFamily = ""; 
    }
}





